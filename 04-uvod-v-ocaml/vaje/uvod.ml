(*----------------------------------------------------------------------------*]
 Funkcija [insert x k list] na [k]-to mesto seznama [list] vrine element [x].
 Če je [k] izven mej seznama, ga funkcija doda na začetek oziroma na konec.
 - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
 # insert 1 3 [0; 0; 0; 0; 0];;
 - : int list = [0; 0; 0; 1; 0; 0]
 # insert 1 (-2) [0; 0; 0; 0; 0];;
 - : int list = [1; 0; 0; 0; 0; 0]
[*----------------------------------------------------------------------------*)

let rec insert el k list=
  if k <= 0 then
    el :: list
  else
    match list with
    | [] -> [el]
    | a :: ostalo -> a :: insert el (k-1) ostalo

(*----------------------------------------------------------------------------*]
 Funkcija [divide k list] seznam razdeli na dva seznama. Prvi vsebuje prvih [k]
 elementov, drugi pa vse ostale. Funkcija vrne par teh seznamov. V primeru, ko
 je [k] izven mej seznama, je primeren od seznamov prazen.
 - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
 # divide 2 [1; 2; 3; 4; 5];;
 - : int list * int list = ([1; 2], [3; 4; 5])
 # divide 7 [1; 2; 3; 4; 5];;
 - : int list * int list = ([1; 2; 3; 4; 5], [])
[*----------------------------------------------------------------------------*)

let rec divide' k (sez1, sez2) =
  if k <= 0 then
    (sez1, sez2)
  else
    match sez2 with
    | [] -> (sez1, [])
    | a::ostalo -> divide' (k-1) (sez1 @ [a], ostalo)

let rec divide k list =
  divide' k ([], list)

let rec divide'' k sez =
  if k <= 0 then
    ([], sez)
  else
    match sez with
    |x :: xs -> 
      let (prej, potem) = divide (k-1) xs in
       (x :: prej, potem)
    |[]->([], [])

(*----------------------------------------------------------------------------*]
 Funkcija [rotate n list] seznam zavrti za [n] mest v levo. Predpostavimo, da
 je [n] v mejah seznama.
 - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
 # rotate 2 [1; 2; 3; 4; 5];;
 - : int list = [3; 4; 5; 1; 2]
[*----------------------------------------------------------------------------*)

let rec rotate k list =
  if k <= 0 then
    list
  else
    match list with 
    | [] -> []
    | a::ostalo -> rotate (k-1) ostalo @ [a]

(*----------------------------------------------------------------------------*]
 Funkcija [remove x list] iz seznama izbriše vse pojavitve elementa [x].
 - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -
 # remove 1 [1; 1; 2; 3; 1; 2; 3; 1; 1];;
 - : int list = [2; 3; 2; 3]
[*----------------------------------------------------------------------------*)

let rec remove' el (list1, list2) =
  match list2 with
  | [] -> list1
  | el' :: ostalo when el' = el -> remove' el (list1, ostalo)
  | a :: ostalo -> remove' el (list1 @ [a], ostalo)

let rec remove el list = remove' el ([], list)



